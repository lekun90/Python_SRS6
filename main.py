print(",".join(map(lambda x: f'{int(x)} руб. {(x - int(x)) * 100:02.0f} коп.', sorted(numbs, reverse=True))))
print('Цены, отсортированные по убыванию:')
res = list(map(lambda x: f'{int(x)} руб. {(x - int(x)) * 100:02.0f} коп.', sorted(numbs, reverse=False)))
